class Pokemon {
  final String name;
  final List<String> abilities;
  final int baseExperience;
  final int height;
  final String image;
  final List<String> types;
  final int weight;
  final String color;

  Pokemon({
    required this.name,
    required this.abilities,
    required this.baseExperience,
    required this.height,
    required this.weight,
    required this.image,
    required this.types,
    required this.color,
  });
}
